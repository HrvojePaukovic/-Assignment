#include <iostream>

int main() {
    // Step 1: Initialize variables
    int a = 10, b = 20, c = 30;

    // Step 2: Add a and b, store result in d
    int d = a + b;
    std::cout << "Value of d (a + b): " << d << std::endl;

    // Step 3: Subtract c from d, store result in e
    int e = d - c;
    std::cout << "Value of e (d - c): " << e << std::endl;

    return 0;
}




